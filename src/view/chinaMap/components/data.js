const badData = ["江宁区"];


export function setMap(region) {
  // console.log(region)

  if (badData.includes(region.name)) {
    return;
  }

  mapConfig.geo.map = region.name;
  mapConfig.geo.center = region.center;
  mapConfig.series[0] = setGeoConfig(region.name, region.center, false);

  mapConfig.backgroundColor = "#242A38"
  return mapConfig;
}